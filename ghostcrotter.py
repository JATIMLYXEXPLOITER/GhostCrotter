def find_parameters(url, ssl_verify=True):
    try:
        response = requests.get(url, verify=ssl_verify, timeout=10, hearders={"User-Agent": ua.random})
        if response.status_code == 200:
            parsed_url = urlparse(url)
            params = parse_qs(parsed_url.query)
            param_matches = re.findall(r'[?&]([a-zA-Z0-9_]+)=', response.text)
            for param in param_matches:
                if param not in params:
                    params[param] = [""]
            return list(params.keys())
    except requests.RequestException as e:
        log_message(f"Error finding parameters: {e}", "ERROR")
    return []

def test_payload(url, params, payload, ssl_verify=True):
    for param in params:
        try:
            response = requests.get(url, params={param: payload}, verify=ssl_verify, timeout=10, headers={"User-Agent": ua.random})
            if response.status_code == 200:
                return response.text, param
        except requests.RequestException as e:
            log_message(f"Error testing payload: {e}", "ERROR")
    return None, None
    
def detailweb(url, ssl_verify):
    try:
        response = requests.get(url, verify=ssl_verify, timeout=10, headers={"User-Agent": ua.random})
        soup = BeautifulSoup(response.text, "html.parser")
        title = soup.title.string if soup.title else "No title found"
        meta_desc = soup.find("meta", attrs={"name": "description"})
        meta_desc = meta_desc["content"] if meta_desc else "No description found"
        log_message(f"Website Title: {title}")
        log_message(f"Website Description: {meta_desc}")
        return {"title": title, "description": meta_desc}
    except Exception as e:
        log_message(f"Error extracting website details: {e}", "ERROR")
        return {"title": None, "description": None}
        
def checkserver(url):
    try:
        response = requests.head(url, timeout=10)
        if 'Server' in response.headers:
            log_message(f"Server Version: {response.headers['Server']}")
            return {"server_version": response.headers['Server']}
        else:
            log_message("Server Version: Not found")
            return {"server_version": "Not found"}
    except requests.RequestException as e:
        log_message(f"Error checking server version: {e}", "ERROR")
        return {"server_version": "Error"}
        
