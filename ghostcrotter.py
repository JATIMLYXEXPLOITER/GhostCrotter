def crawler(url, ssl_verify):
    log_message("Starting web crawler...")
    visited_urls = set()
    results = []
    queue = Queue()
    queue.put((url, 0))
    max_depth = 3

    # Parse the domain of the root URL
    root_domain = urlparse(url).netloc

    def is_valid_url(url):
        parsed = urlparse(url)
        # Only allow URLs within the same domain and proper schemes
        return (parsed.netloc.endswith(root_domain) and 
                parsed.scheme in ["http", "https"])

    def fetch_page(url):
        try:
            response = requests.get(url, timeout=10, verify=ssl_verify, headers={"User-Agent": ua.random})
            if response.status_code == 200:
                return response.text
        except requests.RequestException as e:
            log_message(f"Failed to fetch {url}: {e}")
        return None

    def parse_links(html, current_url):
        soup = BeautifulSoup(html, 'html.parser')
        links = set()
        for tag in soup.find_all('a', href=True):
            href = tag['href']
            full_url = urljoin(current_url, href)
            if is_valid_url(full_url) and full_url not in visited_urls:
                links.add(full_url)
        return links

    def crawl():
        while not queue.empty():
            current_url, depth = queue.get()
            if depth > max_depth or current_url in visited_urls:
                queue.task_done()
                continue

            log_message(f"Crawling: {current_url} (depth {depth})")
            html = fetch_page(current_url)
            if html:
                visited_urls.add(current_url)
                links = parse_links(html, current_url)
                for link in links:
                    queue.put((link, depth + 1))
                results.append({"url": current_url, "depth": depth})
            queue.task_done()

    threads = []
    for _ in range(5):  # number threads
        thread = threading.Thread(target=crawl)
        thread.start()
        threads.append(thread)

    queue.join()
    for thread in threads:
        thread.join()

    log_message("Web crawler completed.")
    return results
                        
    
