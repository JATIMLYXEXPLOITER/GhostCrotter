def fetch(raw_url):
    try:
        response = requests.get(raw_url, timeout=10, headers={"User-Agent": ua.random})
        if response.status_code == 200:
            return response.text.splitlines()
        else:
            log_message(f"Failed to fetch payloads, status code: {response.status_code}", "ERROR")
            return []
    except requests.RequestException as e:
        log_message(f"Error fetching payloads: {e}", "ERROR")
        return []
        
